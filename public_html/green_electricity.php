					Giant King Grass has energy content, physical properties and ash properties very similar to corn straw that is used routinely as a fuel in biomass power plants. VIASPACE is pursuing opportunities for co-located Giant King Grass plantations and biomass power plants. The fast-growing nature and proven, dependable energy characteristics of Giant King Grass allows power plants to run 24 hours a day, seven days a week without interruption or variable output due to availability or inconsistent performance of the fuel.<br /><br />

					Direct combustion (or "direct-fired") systems burn biomass in boilers to produce high pressure steam. The steam turns a turbine connected to a generator-the same kind of steam-electric generator used in fossil fuel power plants. As the turbine rotates, the generator turns, and electricity is produced. This is the simplest and oldest way to generate electricity from biomass. To increase the energy-producing efficiency of direct combustion, power plants also operate cogeneration facilities, which capture waste heat and "secondary" steam and use it to heat buildings and provide steam and heat for industrial processes such as ethanol production or drying of chemical and wood products.<br /><br /><br />			
					
