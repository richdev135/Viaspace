					<b>Anaerobic digestion</b> is a series of biological processes in which microorganisms break down biodegradable material such as Giant King Grass in the absence of oxygen. The main output is methane rich biogas, which is combusted to generate electricity and heat, or can be processed into renewable natural gas and transportation fuels. The byproduct of anaerobic digestion is organic fertilizer that can be sold or put back on the Giant King Grass plantation. Anaerobic digestion is similar to the process that occurs in a cow stomach. 
					<br /><br />
					<div class="figureright">
					  <img src="images/greenelectricity/anaerobicdigestion600.jpg" alt="anaerobic digestion" width="600px">
					  <p></p>
					</div>
					<br />
					Giant King Grass is an excellent feedstock for anaerobic digestion. It can be digested alone, or combined with other organic feedstock as shown in the figure above. Giant King Grass has been extensively tested for anaerobic digestion and the output of the digester is biogas which is approximately 60% methane and 40% carbon dioxide. This biogas can be used directly to fuel an internal combustion engine that turns a generator to produce electricity.
					<br /><br />
					<div class="figure">
					  <img src="images/greenelectricity/biogaspowerplant600.jpg" alt="biogas power plant" width="350px">
					  <p>Biogas power plant. The domed structures are the digester<br/> and the engine generator is in the blue structure in the<br/> foreground with a typical engine generator shown in the insert.</p>
