					Agricultural waste and Giant King Grass have different properties than coal and require a different boiler technology. Biomass power plants are usually 10 to 30 MW in size which is much smaller than coal power plants at 500 to 2000 MW. Most of the world's biomass power plants use direct combustion to produce renewable and low carbon electricity. Existing coal power plants can be readily modified to replace up to 20% of their coal with biomass pellets, but their boiler technology cannot accommodate 100% biomass.

					In a warm area, Giant King Grass that is co-located with the biomass power plant can utilize year round--just-in-time-- harvesting to eliminate the need for baling and long-term storage of the fuel. This is a major logistical advantage that lowers costs. Direct combustion power plants have a much lower capital investment than solar power, and equipment is widely available. By using high yielding Giant King Grass, the cost of electricity is much cheaper than electricity produced using diesel fuel or solar electricity. Furthermore, biomass can produce low carbon electricity 24 hours a day which is needed for "base power" applications.
