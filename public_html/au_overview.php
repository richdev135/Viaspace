								<p>VIASPACE Green Energy is pursuing long-term contracts to supply Giant King Grass as a feedstock for green energy production.  As an example of recurring revenue, a single modest 30 megawatt (MW) grass-powered electricity generating plant requires 460 tons of grass every day for its estimated 30 to 50 year life.</p> 

