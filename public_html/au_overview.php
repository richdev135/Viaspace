					<h2>Financial Strategy</h2>
					<p>VIASPACE has moved aggressively to support and focus on its core green energy business. To improve its financial position, the Company has reduced operating expenses, executed an acquisition with positive cash flow, and sold unprofitable non-core business lines.</p>

					<p>In 2008 VIASPACE acquired Inter-Pacific Arts (IPA), a profitable framed art business, for its material ongoing revenue, net income and positive cash flow; and for its proprietary license to grow and sell Giant King Grass. To generate additional near-term revenue, crops of Giant King Grass will be sold as animal feed for dairy cows, cattle, pigs, sheep, poultry and fish. The company is pursuing long-term supply contracts for animal feed.</p>  

					<p>In order to increase its focus on renewable and alternative energy, VIASPACE sold—for cash and debt forgiveness—its non-core businesses in security products and humidity sensors. These strategic moves streamlined operations, cut expenses, and released resources to dedicate to energy projects.</p>

					<p>These financial steps have made a positive impact on the Company’s balance sheet and income statement.</p>

