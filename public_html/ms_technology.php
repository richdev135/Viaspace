					<p>Ionfinity's technology utilizes inventions developed by scientists at the NASA/Jet Propulsion Laboratory and licensed from Caltech. Current ionization techniques ionize only 1-2% of the sample. The SIM ionizes very efficiently, which translates into a 10x increase in sensitivity. Current ionization methods are somewhat violent and tend to fracture samples being ionized into fragments. In tradition analysis then the mass spectrometer analyzes the mass of the fragments, which then must be reconstructed to the original sample from fragments. Fragmentation introduces considerable uncertainty into the analysis. The SIM ionizes the sample without fragmentation - thus its designation as ‘soft' ionization. The SIM is tiny and compatible with portable and handheld systems. The SIM allows molecules to be detected at extremely low concentrations, which benefits all traditional applications. More importantly, it enables new applications such as ‘sniffing' for traces of chemical and biological agents, narcotics and explosives.</p>

