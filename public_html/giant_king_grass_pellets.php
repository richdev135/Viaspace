					The reason for the limitation to 20% for co-firing is that the boilers in the coal power plant are generally not compatible with larger amounts of biomass. New power plants with different boiler technology that use 100% biomass are now widely available and receiving a lot of attention due to their green technology.

					Torrefaction is an emerging technology that converts biomass into bio coal that can be directly substituted in any amount for coal in existing coal-fired power plants. Bio coal has high energy density and can be stored outdoors just like coal.<br /><br />
					
					<h2>Biomass Pellets-Overview</h2>					
					Biomass is inherently bulky and difficult to transport and store -- think of a bale of hay. Shipping bales of hay from a plantation in Asia or Africa to a customer in Europe or the US is not practical. The volume of the biomass must be reduced in a process called densification, and pellets are a good example. A pellet is simply biomass that is dried, made into a powder and pressed under high pressure into a pellet that is about the thickness of a pencil and 1 inch long. For large-scale applications, pellets are shipped in bulk in the hold of a ship, and handling is similar to the handling of bulk rice or grain.<br /><br />

