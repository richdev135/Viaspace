					<h2>Properties of Biomass Pellets</h2>
										
					Pellets have several advantages compared to their raw biomass feedstock. The pellets are more energy dense and easier to transport and feed into automated feed systems. These advantages, when combined with the renewable and environmentally sound properties of the feedstock, make biomass pellets a viable source of green energy.<br /><br />

					A wide array of biomass materials can be used to manufacture pellets. Most pellets today are made from sawdust and other wood waste. The demand for pellets has exceeded the amount of wood and agricultural waste available, and dedicated energy crops such as Giant King Grass are strong alternatives. Pellet mills are commercial enterprises, and equipment for making pellets is available from many sources.<br /><br />

					A biomass briquette is similar to a pellet but is larger-- up to 3 1/2 inches in diameter and 1 foot long. Briquettes are often used in industrial applications that generate heat or steam rather than electricity.<br /><br />

