					<h1>Giant King<sup>&reg</sup> Grass Pellets & Biocoal to Replace Coal in Existing Power Plants</h1><hr />
					<br/><br/>
					Over 40% of electricity worldwide is generated by coal-fired power plants. Coal has the largest carbon dioxide emissions per unit energy of all fossil fuels, and coal-fired power plants are a major source of man-made carbon dioxide emissions. Coal-fired power plants generally have a life of 20 to 50 years and represent a huge global capital investment. It is not economically feasible to retire these coal-fired power plants and replace them with greener technology. It is possible, however, to reduce their carbon dioxide emissions by replacing some of the coal with biomass - burning both fuels together which is called co-firing. 
					<br/><br/>

					Several European countries and Japan and Korea have mandated carbon dioxide emissions reductions from their power plants and have provided incentives, subsidies and feed in tariffs that make co-firing of biomass with coal financially feasible. Biomass pellets are considerably more expensive than coal and would not be an attractive option on price alone.
					<br/><br/>

					An older and larger market for pellets is for residential and business heating in winter. Some countries have district heating where the heat is generated in a central location and distributed in the form of hot water or steam. Combined heat and power (CHP) systems generate both heat and electricity. There are also residential heating systems designed for pellets and these can be domestic boilers that generate hot water, or simple pellet stoves.
					<br/><br/>
					The vast majority of pellets produced and consumed in the world are made from wood.
