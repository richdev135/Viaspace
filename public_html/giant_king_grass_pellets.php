					For comparison, 2015 the worldwide coal production was about 7500 million metric tons-- 278 times larger than the global biomass pellet reduction.  Europe is the largest producer of biomass pellets, and also the largest consumer. The demand for pellets in Europe is larger than supply, and Europe is the largest importer of pellets. The United States is the second largest producer of pellets and Canada is third. To assure a supply of biomass for their power plants, several European utilities have set up pellet mills in the US near the source of sawdust and other waste wood.
					Japan and Korea are importing wood pellets, and China is both producing and using pellets.
