					<h1>Giant King<sup>&trade;</sup> Grass Pellets for Coal Replacement</h1><hr />
					
					<b>Co-firing Giant King Grass pellets with coal in existing power plants represents an extremely large green energy market for Giant King Grass.</b><br /><br />
					
					Over 40% of electricity worldwide is generated by coal-fired power plants. Coal has the largest carbon dioxide emissions per unit energy of all fossil fuels, and coal-fired power plants are a major source of man-made carbon dioxide emissions. Coal-fired power plants generally have a life of 20 to 50 years and represent a huge global capital investment. It is not economically feasible to retire these coal-fired power plants and replace them with greener technology. It is possible, however, to reduce their carbon dioxide emissions by replacing some of the coal with biomass - burning both fuels together which is called co-firing.<br /><br />

					<b>A straightforward and modest cost modification can allow most coal-fired power plants to replace up to 20% of the coal currently being used with biomass pellets, and thus reduce the power plant's carbon dioxide emissions by 20%. Co-firing will allow power plants to meet their carbon emissions reductions targets for the next decade. Many coal-fired power plants in Europe have been co-firing for more than 10 years.</b><br /><br />

