					<h2>Biocoal</h2>
					The use of biomass pellets is well established, but has some limitations. Traditional biomass pellets have lower energy density per ton compared to coal, and they cannot be stored outdoors in the rain because they absorb moisture and disintegrate. New processes including torrefaction and hydrothermal catalysis convert biomass into biocoal which is pelletized and has properties very similar to ordinary coal.  It has the same energy density and can be stored outdoors. Biocoal can be mixed in any proportion with coal and pulverized in the same machines. Biocoal burns cleanly and does produce the mercury or sulfur emissions of coal. Giant King Grass has been converted into biocoal and has the same energy density as good metallurgical quality coal.
