					<h2>Extensive Patent Coverage</h2>

					The Caltech patent portfolio includes the fundamental idea of a direct hydrocarbon fuel cell that uses a polymer electrolyte membrane (PEM), construction of the catalysts, anodes, cathodes, and membrane electrode assemblies (MEAs), as well as alternative membranes.  Other patents include water recovery, methanol sensors and filters, monopolar geometry, and electrolysis of methanol to produce hydrogen.  Contact the company for a copy of issued patents. 

					<h2>Partnering to Advance Fuel Cell Adoption</h2>

					<p>DMFCC will work cooperatively with fuel cell manufacturers and OEMs to provide them with the patent protection they need, using DMFCC's "have made",  "import", and licensing rights.  Instead of a license fee, DMFCC prefers to work with an OEM on a cartridge supply arrangement which would allow DMFCC to provide innovative and safe fuel cartridges specific to each fuel cell-powered device manufactured by the OEM.  DMFCC and its partners will manufacture cartridges to an OEM's specifications, certify that the cartridges meet international safety standards, and distribute them on a global basis.  If an OEM also desires to perform any of these functions, DMFCC is willing to work in partnership with the OEM.  Using the cartridge approach, rather than requiring an upfront fee, DMFCC will share the market risk with the OEM and also will provide a valuable service by supplying cartridges to the marketplace.</p><br />

