					<h2>65 Issued & 33 Pending Patents Worldwide</h2>

					<p>The direct liquid hydrocarbon fuel cell was invented and developed by the NASA Jet Propulsion Laboratory (JPL), which is operated by Caltech, and the University of Southern California (USC). Hydrocarbon fuels include methanol, ethanol, formic acid, formaldehyde, dimethoxymethane and others. Methanol is the most common fuel. Caltech has 65 issued and 33 pending patents worldwide on the direct liquid hydrocarbon fuel cell technology. Direct Methanol Fuel Cell Corporation (DMFCC) has license rights to all of these patents. Caltech and USC are shareholders in DMFCC.</p>

