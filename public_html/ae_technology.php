					<h1>DMFC Technology</h1><hr />

					<h3>What is a DMFC?</h3>

					<p>Direct Methanol Fuel Cells (DMFCs) are alternative energy devices which can be used to power a wide range of portable and mobile electronics. DMFCs were invented at the Caltech/NASA Jet Propulsion Laboratory with the University of Southern California (USC).</p> 

					<p>DMFCs are electrochemical devices that convert high energy density fuel (liquid methanol) directly to electricity. They operate silently, at relatively low temperatures and offer much longer operating time than today's batteries. Unlike a battery, DMFCs do not need to be recharged. They can provide electricity continuously to the consumer electronic devices as long as oxygen and fuel are supplied to the fuel cell. To achieve this, DMFCs can be "hot-swapped" and instantly recharged with replacement methanol cartridges (akin to batteries). Furthermore, this eliminates the need for consumers to carry electrical cords or adapters.</p>

					<p>Methanol or methanol/water solution is the industry's choice of fuel. Because it is a liquid, it is easy to store and transport. Methanol is inexpensive and readily available. It also has very high energy density, which means longer operating time.</p>

					<h3>How does a DMFC Work?</h3>

					<p>The heart of a fuel cell consists of catalysts for the electrochemical reaction and a special piece of plastic that can conduct protons. The technical term for this special plastic is polymer electrolyte membrane (PEM) and the most common PEM used in DMFCs today is NafionTM, produced by Dupont. The most common catalysts used are PtRu alloy for the anode and Pt for the cathode.</p>

					<p>In the fuel cell, the fuel is not burned, but rather is converted into electricity through an electrochemical process that splits methanol into protons, electrons, and carbon dioxide at the anode and then combines these protons and electrons with oxygen at the cathode to produce water. It is a very simple concept.</p><br />

