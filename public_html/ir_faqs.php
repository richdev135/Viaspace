					<br /><a name="faqs_0"></a><h3>1. What is VIASPACE?</h2>
					<p>VIASPACE grows Giant King Grass as a low-carbon fuel for electricity generating power plants; for energy pellets; and as a feedstock for bio methane production and cellulosic biofuels, biochemicals and biomaterials. 
					Proprietary Giant King Grass is the highest yielding biomass crop in the world. Bio-electricity, biogas, biofuels, biochemicals, bio plastics, and biomaterials all need biomass as feedstock, and Giant King Grass is suitable for these applications. Because of its high yield, it is a low-cost feedstock. Equally important, Giant King Grass provides the reliable and well-characterized feedstock that is required by banks and investors in order to provide financing for these bioenergy projects. Giant King Grass is a natural plant, and is not genetically modified and not an invasive species.
