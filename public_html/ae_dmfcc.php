					<h2>Patented Clean Energy Technology</h2>

					<p>DMFCC has licensed an extensive portfolio of direct methanol fuel cell patents from Pasadena-based California Institute of Technology (Caltech) and the University of Southern California (USC). Caltech manages NASA's Jet Propulsion Laboratory (JPL) where the direct methanol fuel cell was invented in collaboration with professors at USC.  The 65 issued and 33 pending patents cover direct liquid hydrocarbon fuel cells (including direct methanol fuel cells), electrode construction, membrane electrode assemblies (MEAs), fuel cell systems, methanol sensors and filters, water recovery, monopolar geometry, and electrolysis of methanol to form hydrogen. Access to these patents is needed for companies making, using or selling direct methanol fuel cells.</p>

					<p>DMFCC also has several patent applications pending on disposable fuel cartridges, their valves and connectors to fuel cells.</p>

					<h2>Business Partners</h2>

					<p>Direct Methanol Fuel Cell Corporation and its partners are working to develop the worldwide infrastructure to manufacture, fill and distribute disposable methanol fuel cartridges.  This global capability is needed to support widespread adoption of fuel cell powered devices. DMFCC’s cartridge manufacturing partners are experienced and trusted suppliers to a wide range of industries including electronics OEMs.</p>

					<p>Through its patent portfolio, DMFCC also offers patent protection for OEMs and manufacturers of direct methanol and other liquid hydrocarbon fuel cells. DMFCC will partner with fuel cell and electronics manufacturers worldwide to bring their fuel cell products to market within cost-effective parameters. DMFCC  develops custom-designed cartridges for partners’ varying applications while ensuring the finished products meet technical and safety requirements and are available at an attractive price.</p> 

					<p>DMFCC is partnered with Samsung and other companies engaged in fuel cell development and applications. To support the ongoing needs of our customers—including commercial introduction of liquid fuel cells as an economical and environmentally friendly alternative to traditional batteries—DMFCC is forming a worldwide supply infrastructure of cartridge manufacturing, methanol filling and distribution.</p>

					<p>With state of the art proprietary products, a global network of manufacturing partners, and a comprehensive suite of fundamental fuel cell patents, DMFCC has a mechanism in place to commercialize this exciting new technology. Since the cartridges are a disposable consumable item, they are analogous to razor blades and printer cartridges and, as such, represent a significant ongoing recurring revenue stream. According to Frost & Sullivan, the market for commercial fuel cells for portable electronics devices is expected to reach more than $600 million by 2013.</p><br />

