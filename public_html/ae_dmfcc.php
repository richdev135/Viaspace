					<p>Fuel cells are electrochemical devices that efficiently and cleanly convert a fuel, such as methanol, into electricity without burning. The fuel cell is permanently built into a notebook computer in the space where the battery is now. A small disposable fuel cartridge is inserted into the computer which provides the fuel for the fuel cell.</p>
					
					<p>The advantages of a fuel cell are longer-lasting power compared to a rechargeable battery, instantaneous recharging by simply replacing the disposable fuel cartridge, non-toxic disposal, and light weight. As a result, methanol fuel cells are being developed by many companies as a new, clean alternative power source for portable electronic devices.  Fuel cells offer freedom from the electrical grid which is important for many business and professional applications.</p>

