					The biomass industry has recognized that dedicated energy crops are going to be required as the primary source of fuel or feedstock for their plants in addition to agricultural and forestry waste. These dedicated energy crops can be perennial grasses such as Giant King Grass, fast-growing, short rotation trees similar to those being used in the pulp and paper industry, Jatropha and other plants.<br /><br />

					Among the different types of dedicated energy crops, non-food perennial grasses are practical and effective for producing green energy. Giant King Grass can be grown in areas considered marginal or unsuitable for food crops. Therefore, cultivation of non-food grasses does not compete with food or food-related crops such as corn; nor does it interfere or compete with valuable natural resources such as forests and other timberland. The use of food crops for bio-fuels is being discouraged or banned in almost all countries worldwide.<br /><br />


