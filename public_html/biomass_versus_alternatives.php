					<br/>
					Note that fossil fuel plants in industrialized countries are huge, producing 500 MW of electricity or more. A large biomass plant is 35 MW. A biogas plant could be 1 MW.  In industrialized countries, biomass can be a small, but important niche player. In a developing country or in a remote location a power plant of 1 to 35 MW may be all that is needed and biomass can play a much more important role. In the UK, several coal-fired power plants are being converted to wood pellets and these are large-size.
