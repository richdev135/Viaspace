					<h2>Biomass Compared to Solar and Wind</h2>
					<br/>

					Solar and wind are two largest sources of renewable electricity. 
					<br/><br/>

					The major advantage of both solar and wind is that the source of energy is free. Furthermore operations and maintenance costs are modest, and the cost of the capital investment has come down dramatically. The total cost of solar and wind electricity is mostly paying off the capital investment.
					<br/><br/>

					The major disadvantages of solar and wind are that they are variable and intermittent sources of energy. The amount of electricity produced varies by the season and fluctuates minute to minute. Some of the variability is removed by combining the output of many solar and wind farms located in different areas. Even with combining the output of more than 5000 wind turbines, the figure below shows that wind is still extremely variable.
