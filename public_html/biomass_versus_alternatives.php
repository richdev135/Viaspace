					<h1>Biomass Compared to Fossil Fuels, Solar and Wind</h1>
					<hr />

					<b>Biomass is a low-carbon fuel or energy source that comes from plant material such as wood, corn, sugar cane and grasses such as Giant King Grass. Biomass can be burned to generate electricity and heat, digested to produce bio methane, and converted into liquid biofuels, biochemicals and bio plastics.</b>
					<br /><br />  
					
					Biomass also includes wood and food crops such as sugarcane and corn, and also byproducts such as agricultural waste-for example, corn straw (stover) and rice husks-and food waste such as vegetable oil recycled from cooking processes. Since biomass comes primarily from plants and vegetation that naturally re-grows, biomass is a renewable resource.
					<br /><br />

					Hydroelectricity and geothermal are two other renewable options to produce electricity. They will not be discussed here.
					<br /><br />

					<h2>Biomass vs. Fossil Fuels: Renewable, Low Carbon and Locally Sourced</h2>
					<br />
					<b>
					Fossil fuels (coal, oil and natural gas) have played and continue to play a major role in transforming the world from an agricultural society to the modern industrial world we live in today. Fossil fuels produce electricity, and fuels for heating and transportation.
					<br/><br/>
					Advantages of fossil fuels are that they have been abundant and low cost, and that they provide electricity, heating and cooling, and transportation fuels reliably 24 hours per day and seven days a week.
					<br/><br/>
					Disadvantages of fossil fuels are that there is a limited supply, and there are environmental issues in removing fossil fuels from the ground, transporting them and burning them in power plants or engines. In addition to pollutants like ozone, hydrocarbons, sulfur, mercury, smoke and ash, use of fossil fuels emits large amounts of carbon dioxide into the atmosphere. Some fossil fuels are found in unfriendly countries or in unfriendly environments like under the sea. There are political, economic and environmental issues in obtaining these fossil fuels.
					<br/><br/>
					Fossil fuels release carbon dioxide into the atmosphere.
					<br/><br/>
					</b>


					
					<h3>Biomass Alternatives: Dedicated Energy Crops vs. Waste</h3><br /> 

					<b>The primary requirements that a biomass crop must satisfy to be considered a viable source of green energy are:</b> 

					<ul>
						<li>No conflict with food-crop acreage or food-supply chain</li>
						<li>Scalability to meet demand  for large amounts of electrical energy, bio-fuels, bio-gas, and other green-energy products</li>  
						<li>Continuity of supply with consistent quality</li>
						<li>Low and predictable cost for the 20+ year life of a biomass power plant or biofuel refinery</li>
					</ul>

					Today, biomass electricity is generated using agricultural and forestry waste such as corn stover, wheat straw, rice husks, sawdust, and wood bark. Sometimes municipal solid waste is classified as biomass. Future liquid biofuels are also targeting agricultural waste as a feedstock.<br /><br /> 

					<b>However agricultural waste has several problems:</b> 

					<ul>
						<li>It is seasonal and is not available until the food crop or cotton crop is harvested. At harvest, there is an abundance of waste, but this must be gathered, dried and stored until the next crop is harvested.</li>
						<li>In different seasons, wastes from different crops become available and there is no consistency of the fuel or feedstock.</li>  
						<li>Reliability of supply is uncertain. A farmer may decide from year-to-year what to grow and the waste from the new crop may not be suitable for the biomass application.</li> 
						<li>It is expensive. Although the waste originally had little value to the farmer and was often burned or plowed under, farmers in India, China and Thailand have tripled the price over the last several years.</li>
						<li>The agricultural waste market is a spot market with short-term pricing, and very few long-term contracts have ever been signed. It is difficult to get biomass project financing without an assured source of fuel or feedstock at a known price.</li> 
						<li>Agricultural waste must be collected over a substantial radius-- say 50 km-- and this presents logistic problems.</li>
					</ul>
					 
					Forestry waste also depends on the business cycle. The amount of sawdust and wood scraps available is much lower today because of the reduced homebuilding and other construction due to the economic slowdown.<br /><br />       

					Because of these issues, many biomass power plants in India for example are running at partial capacity because biomass is not available or closed down because the fuel is too expensive to operate profitably.<br /><br />   
					
