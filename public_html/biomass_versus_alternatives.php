					<br/><br/>

					Solar energy depends on the amount of sunlight.  The rising and setting of the sun is predictable, but clouds can dramatically affect the output of a solar farm. The figure below showing electrical output from a small fixed array solar facility in sunny Arizona illustrates the problem.
