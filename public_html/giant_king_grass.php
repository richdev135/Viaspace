					<b>Giant King Grass is carbon neutral.</b> Photosynthesis converts solar energy and carbon dioxide from the atmosphere into plant material (biomass). Burning plant material in a power plant releases the carbon dioxide back into the atmosphere, but it is reabsorbed again when the next crop grows making it carbon neutral.
