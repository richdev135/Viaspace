					<b>Giant King Grass can be burned-either directly or in pellet form as a replacement for coal or oil in electric power plants</b> to produce low-carbon electricity and industrial heat. </br></br>

					<b>Anaerobic digestion of Giant King Grass produces biomethane</b> that fuels an engine generator set to produce electricity or can be injected into the natural gas pipeline and used in many applications including transportation fuel.
