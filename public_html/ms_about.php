					<h2>Markets</h2>

					<p>Ionfinity's new sensor technology has direct application in the defense and homeland security industry, in which approximately $55 billion is spent annually, as well as potential applications in chemical analysis and monitoring for commercial applications, such as environmental monitoring, agriculture and medicine.</p> 
					
					<h2>Technology</h2>

					<p>Ionfinity is developing a "soft-ionization" technology for next-generation mass spectrometers, differential mobility spectrometers and systems dependent on ion-level sampling that can not only revolutionize the traditional applications of MS for industrial process control, biomedical, and environmental monitoring but also lead to new detection systems for homeland security.</p>  

					<p>The latest technology being developed enables detection at levels from ppt (parts-per-trillion) to sub-ppb (parts-per-billion) within 10 seconds and, with miniaturization, by a system no larger than a shoe box. And with projected ruggedness, low power consumption and low cost, the system could be used in many applications where monitoring of air, water or other substances is required.</p>

					<h2>Product Development</h2>

					<p>Ionfinity is developing a compact, stand-alone chemical-sensing device consisting of: 1) a new and powerful detector called a Differential Mobility Spectrometer; 2) a non-radioactive ionization method that does not fragment or multiply-ionize sampled specimens; and 3) a micro-gas chromatograph for confirmation and enhanced detection capability. The device will detect chemicals and materials of interest at ppt (parts-per-trillion) to ppb (parts-per-billion) levels after 6 seconds. The sensor is self contained with respect to power and communications.</p>

