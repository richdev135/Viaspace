					Giant King Grass has been independently tested for biogas yield, and the results are excellent. Anaerobic digesters favor fresh leafy green biomass. For this application, Giant King Grass should be harvested every 30-60 days when it is 3-5 feet tall with an annual yield of 167 tons per acre (375 metric tons per hectare). Drying is not necessary. The laboratory test results show that 1 ton of Giant King Grass produces biogas that contains 5489 standard cubic feet of methane (171 m3/metric ton). The biogas production per ton is 50% higher than elephant grass, and the yield per acre is also much higher. Both factors contribute to the cost advantage of Giant King Grass for biogas production.<br /><br />
					
