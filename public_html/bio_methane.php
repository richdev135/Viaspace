					<h1>Bio-methane: Giant King Grass as Feedstock for Producing Green Renewable Methane</h1><hr />
					
					<b>Bio-methane is produced from Giant King Grass using proven and widely used anaerobic digestion and by second-generation gasification technology. Bio-methane is the "green" energy equivalent of methane, the principal component of natural gas which is widely used for heating, cooking, electricity generation and as a vehicle fuel. Biogas is produced when organic matter decomposes without oxygen (anaerobic digestion). Composed mainly of methane (55%) and carbon dioxide, biogas can also be upgraded to pure methane and inserted into the existing natural gas pipelines for efficient transmission. The byproduct of anaerobic digestion is organic fertilizer that can be put back on the Giant King Grass fields.</b><br /><br /> 
					
