					Power generation and heating are the most common applications for biogas worldwide. Burning biogas in water and steam boilers is an established method of generating heat to serve the needs of industrial installations, or to feed into district heating systems. The green biogas is also used to generate electricity in combustion engines or turbines that turn generators or fuel cells. The most efficient way to utilize biogas is by using combined heat and power, employing a gas engine to generate electricity and capture the heat generated in the process to be used in heating, steam or drying systems.<br /><br />

					With the high yield of Giant King Grass, a biogas plant generating 1 MW of electricity and 1 MW of heat can be fueled by a 170 acre (70 ha) plantation.<br /><br />

